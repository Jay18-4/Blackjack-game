    "        try:\n",
    "            bet = int(self.bet_entry.get())\n",
    "            if bet <= 0 or bet > self.chips.total:\n",
    "                raise ValueError(\"Invalid bet amount.\")\n",
    "            self.chips.bet = bet\n",
